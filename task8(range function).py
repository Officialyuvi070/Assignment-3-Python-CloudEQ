# Explain the range() function in Phython with three parameter start, stop and step-size.

# Expain the For Loop with range() function.

# apply only start parameter.
print("apply only start parameter in range()")
for a in range(10): #Declare the range function.
    print(a)
    
# apply start and stop parameter.
print("Apply Start and stop parameter in range()")
for a in range(1,10): #Declare the range function.
    print(a)
    
# apply start, stop and step-size parameter.
print("Apply Start, stop and step-size parameter in range()")
for a in range(0,16,2): #Declare the range function.
    print(a)

